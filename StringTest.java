/**
 * This is a simple program to display the
 * character in a string.
 *
 * @author sleys
 *
 */
public class StringTest {

	/**
	 * @param args
	 */
	public static void main(String[] args) {

		String me = "I am a computer engineer";

		System.out.print(me.charAt(20));
		System.out.print(me.charAt(19));
		System.out.print(me.charAt(7));
		System.out.println(me.charAt(22) + "!!!");
		System.out.println(me.length());
	}


}
